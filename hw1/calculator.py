def add(x : int, y : int) -> int:
    print(x + y)


def sub(x : int, y : int) -> int:
    print(x - y)


def mult(x : int, y : int) -> int:
    print(x * y)


def div(x : int, y : int) -> int:
    print(x / y)


def mod(x : int, y : int) -> int:
    print(x % y)


def pow(x : int, y : int) -> int:
    print(x ^ y)


if __name__ == '__main__':
    add(4, 5)
    sub(3, 1)
    mult(2, 2)
    div(4, 2)
    mod(5, 3)
    pow(6, 2)